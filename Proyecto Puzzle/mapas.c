/* ============================================================
 *  Lectura de mapas desde archivos .txt (mejora opcional)
 * ============================================================
 *
 *  Cada archivo debe contener FILAS_MAPA (60) lineas de
 *  texto, cada una con exactamente COLUMNAS_MAPA (60)
 *  caracteres que representan una fila del mapa.
 *
 *  Si el archivo no existe, no se puede abrir o esta
 *  incompleto/mal formado, la funcion no modifica el mapa
 *  y se conserva el mapa por defecto declarado arriba.
 * ============================================================ */
static int cargar_mapa(const char *ruta, char mapa[FILAS_MAPA][COLUMNAS_MAPA + 1]) {
    FILE *archivo;
    char  temporal[FILAS_MAPA][COLUMNAS_MAPA + 1];
    char  linea[256];
    int   fila;

    archivo = fopen(ruta, "r");
    if (archivo == NULL) {
        /* No existe el archivo: se conserva el mapa por defecto */
        return 0;
    }

    for (fila = 0; fila < FILAS_MAPA; fila++) {
        int col;
        int len;

        if (fgets(linea, sizeof(linea), archivo) == NULL) {
            /* El archivo tiene menos filas de las esperadas */
            fclose(archivo);
            return 0;
        }

        /* Quitar el salto de linea (\n y/o \r) al final */
        len = (int)strlen(linea);
        while (len > 0 && (linea[len - 1] == '\n' || linea[len - 1] == '\r')) {
            linea[len - 1] = '\0';
            len--;
        }

        if (len < COLUMNAS_MAPA) {
            /* La fila no tiene el ancho esperado */
            fclose(archivo);
            return 0;
        }

        for (col = 0; col < COLUMNAS_MAPA; col++) {
            temporal[fila][col] = linea[col];
        }
        temporal[fila][COLUMNAS_MAPA] = '\0';
    }

    fclose(archivo);

    /* Solo se sobreescribe el mapa si TODO el archivo fue valido */
    memcpy(mapa, temporal, sizeof(temporal));
    return 1;
}

void cargar_mapas(void) {
    cargar_mapa(ARCHIVO_MAPA1, mapa1);
    cargar_mapa(ARCHIVO_MAPA2, mapa2);
    cargar_mapa(ARCHIVO_MAPA3, mapa3);
}